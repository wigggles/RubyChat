#===============================================================================================================================
class World_00 < GameWorld
  #---------------------------------------------------------------------------------------------------------
  def initialize(parent_state)
    super(parent_state)
  end
  #---------------------------------------------------------------------------------------------------------
  def update()
    super()
  end
  #---------------------------------------------------------------------------------------------------------
  def draw()
    super()
    @bgimg = BlobDraw.get_image({
      of: :round_rect, width: @view_width, height: @view_height, radius: 16, outlined: true
    }) if @bgimg.nil?
    @bgimg.draw(@x, @y, 0, 1.0, 1.0, color)
  end
  #---------------------------------------------------------------------------------------------------------
  def dispose()
    super()
  end
end
